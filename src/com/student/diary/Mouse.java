public class Mouse {

    private double x;
    private double y;

    public Mouse() {
        this.x = 0;
        this.y = 0;
    }

    public double getX() {
        return this.x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return this.y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
